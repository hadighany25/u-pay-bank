function applyLanguage() {
  const lang = localStorage.getItem("lang") || "en";

  // 1. ប្តូរអក្សរតាម Tag data-i18n
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = translations[lang][key];
      } else if (el.tagName === "OPTION") {
        // បន្ថែមសម្រាប់ select options
        el.innerText = translations[lang][key];
      } else {
        el.innerText = translations[lang][key];
      }
    }
  });

  // 2. ហៅ updateGreeting តែម្តងគត់នៅខាងក្រៅ Loop
  updateGreeting();

  // 3. ប្តូរ Font
  if (lang === "kh") {
    document.body.classList.add("kh-font");
  } else {
    document.body.classList.remove("kh-font");
  }

  // 4. Update UI Settings
  const langLabel = document.getElementById("currentLangLabel");
  if (langLabel) langLabel.innerText = lang === "en" ? "EN" : "KH";

  if (document.getElementById("check-en")) {
    document.getElementById("check-en").style.display =
      lang === "en" ? "block" : "none";
    document.getElementById("check-kh").style.display =
      lang === "kh" ? "block" : "none";
    document
      .getElementById("lang-en")
      .classList.toggle("active", lang === "en");
    document
      .getElementById("lang-kh")
      .classList.toggle("active", lang === "kh");
  }
}

// បន្ថែមមុខងារនេះចូលក្នុង lang.js របស់បងទៅ
function updateGreeting() {
  const hour = new Date().getHours();
  const greetEl = document.getElementById("greetText");
  if (greetEl) {
    let lang = localStorage.getItem("lang") || "en";
    greetEl.innerText =
      hour < 12
        ? lang === "kh"
          ? "អរុណសួស្ដី,"
          : "Good Morning,"
        : hour < 18
          ? lang === "kh"
            ? "ទិវាសួស្ដី,"
            : "Good Afternoon,"
          : lang === "kh"
            ? "សាយណ្ហសួស្ដី,"
            : "Good Evening,";
  }
}

